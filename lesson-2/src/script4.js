"use strict";

/**
 * Addict n1 + n2
 * @param {number} n1 
 * @param {number} n2 
 * @returns {number}
 */
function Add(n1, n2) {
    return +n1 + +n2;
}

/**
 * Substract n1 - n2
 * @param {number} n1 
 * @param {number} n2 
 * @returns {number}
 */
function Subst(n1, n2) {
    return +n1 - +n2;
}

/**
 * Multiply n1 - n2
 * @param {number} n1 
 * @param {number} n2 
 * @returns {number}
 */
function Multiple(n1, n2) {
    return +n1 * +n2;
}

/**
 * Divide n1 - n2
 * @param {number} n1 
 * @param {number} n2 
 * @returns {number}
 */
function Divide(n1, n2) {
    return +n1 / +n2;
}

